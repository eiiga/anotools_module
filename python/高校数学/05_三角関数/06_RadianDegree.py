import math

# ［事前準備］πの値を変数に格納
pi = math.pi

# 問題1：30°を弧度法で表記
q1 = 30
ans1 = math.radians(q1)
print('問題1：30° -> ', ans1)

# 問題2：120°を弧度法で表記
q2 = 120
ans2 = math.radians(q2)
print('問題2：120° -> ', ans2)

# 問題3：360°を弧度法で表記
q3 = 360
ans3 = math.radians(q3)
print('問題3：360° -> ', ans3)

# 問題4：(3/4)πを度数法で表記
q4 = (3 / 4) * pi
ans4 = math.degrees(q4)
print('問題4：(3/4)π -> ', ans4)

# 問題5：(5/3)πを度数法で表記
q5 = (5 / 3) * pi
ans5 = math.degrees(q5)
print('問題5：(5/3)π -> ', ans5)
